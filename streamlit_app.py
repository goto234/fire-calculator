# ---------- HELPERS ---------- #
def format_inr(val):
    if val >= 1e7: return f"â‚¹{val/1e7:.2f} Cr"
    if val >= 1e5: return f"â‚¹{val/1e5:.2f} L"
    return f"â‚¹{val:,.0f}"

def fig_to_b64(fig):
    buf = io.BytesIO()
    fig.savefig(buf, format="png", bbox_inches="tight", dpi=160)
    plt.close(fig)
    return base64.b64encode(buf.getvalue()).decode()

